/******************************************************************************
 class QryWdbeFilList::VecVOrd
 ******************************************************************************/

uint QryWdbeFilList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "grp") return GRP;
	if (s == "own") return OWN;
	if (s == "fnm") return FNM;
	if (s == "ret") return RET;
	if (s == "reu") return REU;

	return(0);
};

string QryWdbeFilList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == GRP) return("grp");
	if (ix == OWN) return("own");
	if (ix == FNM) return("fnm");
	if (ix == RET) return("ret");
	if (ix == REU) return("reu");

	return("");
};

void QryWdbeFilList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i = 1; i <= 5; i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

