/******************************************************************************
 class QryWdbeUsrList::VecVOrd
 ******************************************************************************/

uint QryWdbeUsrList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "usg") return USG;
	if (s == "ste") return STE;
	if (s == "srf") return SRF;
	if (s == "prs") return PRS;
	if (s == "own") return OWN;
	if (s == "grp") return GRP;

	return(0);
};

string QryWdbeUsrList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == USG) return("usg");
	if (ix == STE) return("ste");
	if (ix == SRF) return("srf");
	if (ix == PRS) return("prs");
	if (ix == OWN) return("own");
	if (ix == GRP) return("grp");

	return("");
};

void QryWdbeUsrList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i = 1; i <= 6; i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

